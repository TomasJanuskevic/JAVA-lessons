
package Lesson2;

public class PrimitiveDataTypes {
    public static void main(String [] args) {
        byte b1 = 10;
        byte b2 = 100;
        
        short s1 = 130;
        
        int il = 500;
        
        long l1 = 100000000000L;
        
        float f1 = 3.14F;
        
        double d1 = 3.14;
        
        char c1 = 'a';
        char c2 = 'A';
        char c3 = '7';
        char c4 = ' ';
        char c5 = 100;
        char c6 = '\u2abc';
        
        boolean bool1 = true;
        boolean bool2 = false;
        
        int a1 = 60;
        
        int a2 = 0B111100;
        int a3 = 074;
        int a4 = 0X3C;
        
        int a5 = 1_000_000;
        
        System.out.println(f1);
        System.out.println(c5);
        System.out.println(c6);
        System.out.println(bool1);
        System.out.println(a4);
        System.out.println(a5);
        
        
    }
    
}
